    private void checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkActionPerformed
       SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String sdate = df.format(Date1.getDate());
        
        try{
            Conn c = new Conn();
            
            if(sdate.equals("")){
                JOptionPane.showMessageDialog(null, "Request Date Cannot Be Empty OR Enter Valid Date");
            }else{
            String query = "select meter_no from meter_request where RequestDate = '"+sdate+"' and status = 'Pending'";
            ResultSet rs = c.s.executeQuery(query);
            
            while(rs.next()){
                meterno.add(rs.getString("meter_no"));
            }
                 
            }
            
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_checkActionPerformed

    private void sendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendActionPerformed
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String sdate = df.format(Date2.getDate());
        String smeter = meterno.getSelectedItem();
        
        try{
            Conn c = new Conn();
            String query = "select * from meter_request where meter_no = '"+smeter+"' and SendingDate = '"+sdate+"' and status = 'Sent'";
            ResultSet rs = c.s.executeQuery(query);
            if(sdate.equals("")){
                JOptionPane.showMessageDialog(null, "Sending Date Cannot Be Empty OR Enter Valid Date");
            }else if (rs.next()){
            
                JOptionPane.showMessageDialog(null, "Already Sent Meter!!");
            
            }else{
            String query1 = "update meter_request set status = 'Sent', SendingDate = '"+sdate+"' where meter_no = '"+smeter+"'";
            c.s.executeUpdate(query1);
                 JOptionPane.showMessageDialog(null, "Meter Sent Successfully!!");
            }
            
            
        }catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_sendActionPerformed

