    private void meternoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_meternoItemStateChanged
        String smeter = meterno.getSelectedItem();
        
        try{
            Conn c = new Conn();
            
            
            String query = "select customer.name, customer.address, customer.city, meter_info.meter_location, meter_info.meter_type, meter_info.phase_code, meter_info.bill_type FROM customer JOIN meter_info  ON customer.meter_no = meter_info.meter_no WHERE customer.meter_no = '"+smeter+"'";
            ResultSet rs = c.s.executeQuery(query);
            
            while(rs.next()){
                name.setText(rs.getString("name"));
                ml.setText(rs.getString("meter_location"));
                address.setText(rs.getString("address"));
                city.setText(rs.getString("city"));
                pc.setText(rs.getString("phase_code"));
                mt.setText(rs.getString("meter_type"));
                bt.setText(rs.getString("bill_type"));
            }
                 
            
            
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_meternoItemStateChanged

