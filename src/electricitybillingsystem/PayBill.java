    private void checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkActionPerformed
        String smeter = meterno.getText();
        String smonth = bmonth.getSelectedItem();
        String syear = year.getText();
        
        
         try {
            Conn c = new Conn();
            ResultSet rs = c.s.executeQuery("select units, totalbill, status from bill where meter_no = '"+smeter+"' and billing_month = '"+smonth+"' and  billing_year = '"+syear+"'");  

            
            if (syear.equals("")) {
                JOptionPane.showMessageDialog(null, "Please Enter a Year!!");
                
            }else if (rs.next()){
                
                unit.setText(rs.getString("units"));
                totalbill.setText(rs.getString("totalbill"));
                status.setText(rs.getString("status"));
                
            }else{
                JOptionPane.showMessageDialog(null, "Please Enter Correct Year and Month!!");
                unit.setText("");
                totalbill.setText("");
                status.setText("");
            }
            
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
         
         try {
            Conn c = new Conn();
            ResultSet ra = c.s.executeQuery("select* from tax ");
            
            
            
            while (ra.next()){
                cpu.setText(ra.getString("cost_per_unit"));
                sc.setText(ra.getString("service_charge"));
                mr.setText(ra.getString("meter_rent"));
                st.setText(ra.getString("service_tax"));
                vat.setText(ra.getString("vat"));
                
            }
            
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_checkActionPerformed

