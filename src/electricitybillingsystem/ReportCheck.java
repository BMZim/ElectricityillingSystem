    private void checkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_checkActionPerformed
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String sdate = df.format(Date.getDate());
        
        try{
            Conn c = new Conn();
            
            if(sdate.equals("")){
                JOptionPane.showMessageDialog(null, "Request Date Cannot Be Empty OR Enter Valid Date");
            }else{
            String query = "select * from customer_report where date_month_year = '"+sdate+"'";
            ResultSet rs = c.s.executeQuery(query);
            
            while(rs.next()){
                meterno.add(rs.getString("meter_no"));
            }
                 
            }
            
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_checkActionPerformed

    private void meternoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_meternoMouseClicked
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String sdate = df.format(Date.getDate());
        String smeter = meterno.getSelectedItem();
        
        try{
            Conn c = new Conn();
            
            
            String query = "select * from customer_report where date_month_year = '"+sdate+"' and meter_no = '"+smeter+"'";
            ResultSet rs = c.s.executeQuery(query);
            
            while(rs.next()){
                text.setText(rs.getString("report"));
                
            }
                 
            
            
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_meternoMouseClicked

    private void replayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_replayActionPerformed
        SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd");
            String sdate = df.format(Date.getDate());
        String smeter = meterno.getSelectedItem();
        String stext = text.getText();
        try{
            Conn c = new Conn();
            
            
            String query = "update customer_report set replay = '"+stext+"' where date_month_year = '"+sdate+"' and meter_no = '"+smeter+"'";
            c.s.executeUpdate(query);
            
            JOptionPane.showMessageDialog(null, "Successfully Replied!!");
            

            
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_replayActionPerformed

