    private void checkMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_checkMouseEntered
        String sdate = ((JTextField)Date.getDateEditor().getUiComponent()).getText();
        if (sdate.equals("")){
            check.setEnabled(false);
        }else{
            check.setEnabled(true);
        }
    }//GEN-LAST:event_checkMouseEntered

    private void replayMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_replayMouseEntered
         String sdate = ((JTextField)Date.getDateEditor().getUiComponent()).getText();
         String stext = text.getText();
        if (sdate.equals("") || stext.equals("")){
            replay.setEnabled(false);
        }else{
            replay.setEnabled(true);
        }
    }//GEN-LAST:event_replayMouseEntered

