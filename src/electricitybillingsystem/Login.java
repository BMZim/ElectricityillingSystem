    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
         String sjobid = jobid.getText();
         String spassword = pass.getText();
         String mainadmin ="221000000", mainpass = "admin";
        
            if(sjobid.equals(mainadmin) && spassword.equals(mainpass)){
                setVisible(false);
                new admin(sjobid, spassword).setVisible(true);
                
            }else{
        try {
            Conn c = new Conn();
            String query = "select * from adminlogin where jobid = '"+sjobid+"' and password = '"+spassword+"'";

            ResultSet rs = c.s.executeQuery(query);
            
            
            
            if (rs.next()) {
                
                setVisible(false);
                new admin(sjobid, spassword).setVisible(true);
            }else if(sjobid.equals("") || spassword.equals("") )  {
                
                     JOptionPane.showMessageDialog(null, "Username And Password Field Cannot Be Empty!!");
                
               
            } 
            else {
                JOptionPane.showMessageDialog(null, "Username Or Password is Incorrect!!");
                jobid.setText("");
                pass.setText("");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
            }                
    }//GEN-LAST:event_loginActionPerformed

